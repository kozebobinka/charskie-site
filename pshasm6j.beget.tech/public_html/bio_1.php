        <p>Евгений Чарский – советский художник, живописец.</p>
        <p>Чарский прошел две основательные школы, чтобы уверенно начинать собственную жизнь. Первая из них — Великая
            Отечественная война, вторая — два художественных учебных заведения: Ростовское художественное училище им.
            М.Б. Грекова и Ленинградская Академия художеств.</p>
        <p>В 1952 году в Академии Евгений защитил свой диплом картиной «И. В. Сталин и Мао Дзэдун» на отлично. Тема
            диплома была актуальной и насущной для того времени, картина сразу обратила на себя внимание. Молодой
            художник использовал все свои профессиональные знания и навыки, чтобы языком живописи передать самое главное
            – исторический смысл и значение встречи двух вождей.</p>
        <p>После окончания института Евгений Чарский возвращается полный сил и творческих замыслов в Ростов-на-Дону. С
            этим городом связаны его детство, юность, здесь он получил свое первое профессиональное образование –
            окончил Ростовское художественное училище в 1939 году, отсюда уходил на службу в армию, был участником в
            Великой Отечественной войне, награжден боевыми наградами.</p>
        <p>Спустя несколько послевоенных лет, Чарский собрался с мыслями и совсем недавними ощущениями времени и написал
            негромкое, небольшое полотно, которое получило широкое признание на Всесоюзной выставке, наполненное сложной
            гаммой человеческих чувств, привнесенных тогда во многие дома безысходным горем минувшей войны.
            «Воспоминание о сыне» (1953 г.) — так называлась эта композиция, наполненная тишиной и печалью, в которой
            доживали свой век два пожилых человека. Казалось, что и сам художник в этом холсте, может быть, был немного
            сентиментальным, еще не встал и не распрямился в полную силу в своем ремесле. Потом, на протяжении всей
            своей творческой жизни, художник не раз еще возвращался к теме отечественной войны.</p>
        <p>Но в эти же пятидесятые годы Евгений Гаврилович твердо шагает по целинным землям, выезжает на Северный
            Кавказ, Крым, Вологодскую область, Ладогу, посещает донские станицы. Крепче, сильнее и выразительнее
            становится его палитра — это можно заметить в его «Станичниках», написанных в 1957 году.</p>
        <p>Как художник донской темы, Евгений Чарский наиболее ярко раскрылся в таких композициях, как большой холст
            «Сентябрь на Дону» и полотно «У парома», созданных в 60-е годы.</p>
        <p>Также художник достаточно часто обращался в своем творчестве и к непростой и нелегкой теме событий Октября в
            России и образам его вождей. И надо сказать, что это были работы достаточно суровые по своим трактовкам и
            без налета ходульности и сусальности. Натура Чарского-художника была способна к преодолению того, что уже
            пройдено, и жажда к новым ощущениям в творчестве была присуща ему буквально с начала творческого пути.
            Свидетельством тому может служить почти монументального письма холст «Ровесники века», созданный живописцем
            в 1967 году. Конечно же он выслушивал от коллег упреки в том, что отступил от, всем привычного образа
            «донского живописца Евгения Чарского». Но он шел своим путем и считал, что ходить по хорошо вытоптанной
            дорожке серьезному художнику негоже.</p>
        <p>В своём искусстве, как и в жизни, Евгений мог быть спокойным и размышляющим, импульсивным и резким. Но все
            это не противоречило его образу и концепции живописца со сложившимся характером, умеющего убежденно
            отстаивать свои принципы творца.</p>
        <p>Работы Е. А. Чарского находятся в Ростовском, Таганрогском, Архангельском, Ставропольском, Волгоградском
            музеях изобразительных искусств, в различных краеведческих музеях России, а также в европейских, японских,
            американских и российских частных коллекциях.</p>

