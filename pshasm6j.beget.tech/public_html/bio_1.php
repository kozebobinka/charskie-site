        <h2>КРАТКАЯ БИОГРАФИЯ</h2>
        <p><strong>1919-1993 гг.</strong> — родился 7 марта 1919 г. в г. Китайгород на Украине. Умер 13 января 1993 г. в г.
            Ростове-на-Дону</p>
        <p><strong>1925 г.</strong> — приехал с родителями в г. Ростов-на Дону</p>
        <p><strong>1932-1939 гг.</strong> — учился в Ростовском художественном училище имени М. Б. Грекова</p>
        <p><strong>1939 г.</strong> — сдал экзамены в Ленинградский институт живописи, скульптуры и архитектуры им. И. Е. Репина. Призван
            в Советскую армию</p>
        <p><strong>1941-1945 гг.</strong> — участвовал в ВОВ. Награжден медалями «За оборону Кавказа» и «За победу над Германией»</p>
        <p><strong>1946-1952 гг.</strong> — учился в Ленинградском институте живописи, скульптуры и архитектуры имени И. Е. Репина у
            профессоров А. А. Авилова, Р. Р. Френца. Закончил с отличием.</p>
        <p><strong>1952-1955 гг.</strong> — преподавал в училище им. М. Б. Грекова в Ростове-на-Дону</p>
        <p><strong>1952 г.</strong> — творческая поездка по Волге</p>
        <p><strong>1954 г.</strong> — творческая поездка на целинные земли Ставрополья</p>
        <p><strong>1957 г.</strong> — творческая поездка на Дон</p>
        <p><strong>1965 г.</strong> — творческая поездка в Крым</p>
        <p><strong>1968-1969 гг.</strong> — творческая поездка в Вологодскую область</p>
        <p><strong>1979 г.</strong> — творческая поездка в Горки Ленинские</p>
        <p><strong>1985 г.</strong> — награжден «Орденом Отечественной войны второй степени»</p>
        <p><strong>1946-1980 гг.</strong> — ежегодное участие в Ростовских городских, областных, а также зональных, республиканских,
            Всесоюзных и международных выставках советского искусства</p>
